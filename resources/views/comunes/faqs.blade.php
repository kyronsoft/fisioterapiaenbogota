                    <h3>¿Que es mejor, utilizar hielo o calor, o ambos?</h3>
                    <p>Utilizar hielo es solo conveniente en las primeras 48-72 horas de una lesión para prevenir y
                        controlar la inflamación inicial después de un trauma o lesión.  Pasado este tiempo es
                        conveniente utilizar calor si hay síntomas de dolor y tensión muscular.  Pero abusar del
                        calor tiende a causar inflamación por lo cual es mejor limitar su uso a 15-20 minutos al
                        día.  Alternar el uso de hielo y calor en formas y tiempos muy específicos es más efectivo
                        que cualquiera de estos dos utilizado por si solo en casos de inflamación crónica.</p>
                </div>
                <div>
                    <h3>¿Estirar antes o después del ejercicio?</h3>
                    <p>Realizar estiramientos es solo prudente al finalizar el ejercicio o práctica deportiva. 
                        Estirar antes de la actividad física te predispone a lesiones durante el ejercicio.  Antes
                        de la práctica de ejercicio o deporte lo prudente es calentar muy bien y preparar tu cuerpo
                        para la actividad a la que va a ser sometido.</p>
                </div>
                <div>
                    <h3>¿Cuál es la mejor posición para dormir?</h3>
                    <p>Dormir boca arriba y de lado son las posiciones más cómodas y saludables puesto que ponen
                        menos estrés sobre las diferentes articulaciones del cuerpo.  Sin embargo, si tienes dolor
                        de hombro o de cadera dormir de lado aumentara el dolor.  En ese caso, dormir boca arriba
                        sería la mejor posición para dormir.</p>
