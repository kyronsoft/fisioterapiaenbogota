                    <p>Problemas de balance y equilibrio existen a cualquier edad dependiendo de antecedentes
                        traumáticos y si estos recibieron tratamiento médico y/o terapéutico en su momento, nivel de
                        actividad física, déficits de fuerza, flexibilidad y coordinación neuromuscular.  Con la
                        edad, por que el sistema neuromuscular reacciona más lento y porque con frecuencia el nivel
                        de actividad físico se ha reducido esto genera un deterioro acelerado del balance y
                        equilibrio.  Pero la pérdida del balance se puede mejorar con ejercicios adecuados y mucha
                        constancia.  Hay personas de 80+ años físicamente activas con un balance que supera el de
                        algunas personas de 40-50 años.  Es decir, si bien hay una predisposición a que el balance
                        se reduzca con el pasar de los anos, es un factor altamente determinado por el nivel de
                        actividad física de una persona y totalmente influenciable.  Los problemas de balance pueden
                        mejorar con tratamiento.</p>
