                    <h3>¿La acupuntura es realmente efectiva?</h3>
                    <p>Si.  Esta técnica ha existido por más de 5.000 años por una razón, funciona.  Por muchos años
                        la ciencia no lograba explicar los mecanismos implicados en su efectividad por lo que la
                        denominaban terapia placebo o simplemente inefectiva, a pesar de que la gente una y otra vez
                        reportaba mejoría de sus síntomas y salud en general.  Más recientemente, la ciencia ha
                        podido medir y demostrar que existen cambios de conducción eléctrica en los puntos de
                        acupuntura a pesar de que al abrir un cadáver, no exista evidencia física de que un punto de
                        acupuntura existe.  Igualmente se han demostrado cambios bioquímicos generados en el
                        organismo como la liberación de endorfinas, encefalinas y opioides por parte del cuerpo
                        mismo.  Estas sustancias tienen la función de reducir el dolor.  La Organización de la Salud
                        (OMS), recomienda con alto nivel de evidencia científica el uso de la acupuntura para una
                        extensa lista de problemas, siendo muchos de ellos el tratamiento del dolor.  Con la crisis
                        opioide en el mundo la acupuntura es #1 en el tratamiento del dolor crónico.  Además, carece
                        de efectos secundarios y no genera dependencia.</p>
