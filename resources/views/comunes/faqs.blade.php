                        circulación, movilidad y elasticidad.  La fisioterapia utiliza la postura y el movimiento
                        del cuerpo humano para restaurar o mejorar al máximo la habilidad de realizar nuestras
                        actividades diarias, laborales y recreativas con el más alto nivel de facilidad y confort
                        para mantener una buena calidad de vida.  La Terapia Física puede utilizar técnicas pasivas
                        como diferentes técnicas de movilización de los tejidos blandos (masaje), taping, calor,
                        frio, electro estimulación, ultrasonido, laser entre otros; y utiliza técnicas activas como
                        el ejercicio físico, la reeducación postural y del movimiento corporal, educación en estilos
                        de vida saludables y ergonomía.</p>
