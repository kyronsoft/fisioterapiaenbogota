                    <h3>¿Cómo funciona la acupuntura y para qué sirve?</h3>
                    <p>La acupuntura utiliza la ciencia de los puntos y meridianos energéticos por los que circula
                        el Qi (energía de vida) en nuestro cuerpo.  Es un sistema muy complejo que al ser estimulado
                        adecuadamente a través del uso de agujas de acupuntura restaura el balance energético de
                        todo el sistema del cuerpo humano produciendo efectos benéficos y con frecuencia una cura
                        total y permanente.  Al igual que la Fisioterapia, los efectos de la acupuntura son
                        acumulativos, es decir requieren varias sesiones de tratamiento para lograr y mantener
                        resultados.   Resolver estancamientos y patrones de movimiento energético que se han venido
                        gestando por años y que han causado los síntomas físicos tarda tiempo en resolver. 
                        Restaurar el balance de un sistema que está sufriendo no es arte de magia pero en bastantes
                        casos es posible.  En algunos casos, la persona nota mejoría y hasta desaparición de sus
                        síntomas en una sola sesión.  Esto varía de persona a persona y según el tipo de problema a
                        tratar.  Sin embargo, incluso en este tipo de “curación milagrosa” en una sesión puede que
                        sean necesarias más sesiones posteriores para tratar la causa y no solo el síntoma del
                        problema. Esto solo lo puede determinar un acupunturista entrenado y variará con cada
                        paciente, su problema y respuesta al tratamiento.  La acupuntura tiene múltiples
                        aplicaciones en el contexto médico, odontológico, psicológico y veterinario.  En el contexto
                        Ortopédico y Fisioterapéutico que nos concierne aquí es útil para el manejo del dolor, de
                        lesiones deportivas y para bajar de peso.  La Acupuntura y las Terapias Alternativas en
                        general complementan la medicina y las terapias occidentales mas no las sustituyen.</p>
