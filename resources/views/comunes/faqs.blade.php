                    <h3>¿Es cierto lo que dice la expresión “no pain no gain” que traduce, sin dolor no hay
                        ganancia?</h3>
                    <p>Depende de qué tipo de dolor estamos hablando.  Es importante diferenciar si el dolor que se
                        está experimentando es dolor producto del ejercicio realizado o por uso de técnica
                        inapropiada de los ejercicios y lesión de tejidos.  El dolor generado por el ejercicio
                        físico que es simétrico (en ambos lados), que ocurre uno o dos días después del ejercicio y
                        se desvanece en uno o dos días es normal y no implica lesión.  El dolor asimétrico (en un
                        solo lado, una sola articulación) que no mejora después de un par de días y que persiste por
                        semanas sugiere la posibilidad de una lesión y debe ser valorado y tratado por un
                        profesional de la salud para evitar complicaciones a futuro.</p>
                </div>
                <div>
                    <h3>Los ejercicios me dejan adolorido, no me gusta sentirme así después del ejercicio</h3>
                    <p>Como dijimos en la pregunta anterior, este tipo de dolor no es señal de lesión pero si el
                        dolor es muy fuerte y limita tu habilidad de moverte normalmente, es un indicador de que la
                        intensidad del ejercicio realizado fue mucho más alta de lo que tu cuerpo está acostumbrado
                        a realizar normalmente.  En ese caso, reducir un poco la intensidad de tus ejercicios es lo
                        apropiado.</p>
