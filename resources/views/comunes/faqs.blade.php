                    <h3>¿Que es y cómo funciona la fisioterapia?</h3>
                    <p>La fisioterapia utiliza la postura y el movimiento del cuerpo humano para restaurar o mejorar
                        al máximo la habilidad de realizar nuestras actividades diarias, laborales y recreativas con
                        el más alto nivel de facilidad y confort para mantener una buena calidad de vida.  La
                        Fisioterapia o Terapia Física utiliza técnicas naturales que al ser utilizadas repetidamente
                        por un periodo de tiempo generan efectos acumulativos que enseñan a tu cuerpo formas de
                        moverse más saludables.  Como el efecto es acumulativo, dos o tres sesiones de Fisioterapia
                        no son suficientes para alcanzar ese estado en que el cuerpo memoriza la nueva postura o
                        movimiento hasta que lo hace automático.  Romper malos hábitos que se han creado en años
                        toma tiempo para ser reemplazados por hábitos nuevos.</p>
